<h1>Surfing the World Wide Waves (WWW)</h1>
<h2>The Evolution of Online living.</h2>
        <p>A few years ago the World Wide Web was a far less populous, 
          far less interactive place. Communities grew and free websites started 
          being popular. I remember travelling through hundreds of sites seeing 
          entire communities made of streets, towns and more. In particular I 
          remember geocities and the URL's people had. At the time my website 
          was /athens/forum/1595/. It was a forum for ancient civilizations, a 
          place regrouping these individuals. Each of these was nothing more than 
          a collection of interconnected webpages with banners to generate content 
          and visitors. Over time geocities was bought by yahoo, changed from 
          communal streets to being personalised names and finally being made 
          into nothing. As this occured geocities died away.</p>
        <p>Last year was seen as the year of the blog, the year when 
          blogging became far more important, when it became fashionable to have 
          this creation refered to as a blog. Blogs are like an online diary, 
          you write some text as if you were sending an e-mail, post it and automotically 
          it's dated and timed. In this way the content is instantly indexed. 
          As the days and the weeks progresses the index page expands and months 
          appear, first one then two, three until in the future there may be many 
          posts. </p>
        <p>These blogs have made the web even more accesible than 
          it was two years ago because they are easy to take care of and handle. 
          For those with a little more experience with webmastering new possibilities 
          occur, for example I have added a plog (project log) to this website. 
          It's a piece of software which allows me to create blogs, other people 
          to register and rss feeds are possible. </p>
        <p>I currently use the plog software for two things, firstly 
          as a way of keeping the <a href="whats.htm">what's 
          new</a> section up to date but also for the <a href="warza.php">Warza</a> 
          character I have created on <a href="http://www.kraland.org">Kraland</a>. 
          They are practical because they are easy to keep up to date with no 
          additional software. I also enjoy the ability to add them on my yahoo 
          to keep an eye on my threads. Other people may also add these rss feeds 
          to their website, making it easier to keep up to date with any recent 
          changes. </p>
